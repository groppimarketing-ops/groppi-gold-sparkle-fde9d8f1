function deepSet(obj: any, path: string, value: any) {
  const keys = path.split('.');
  let cur = obj;
  for (let i = 0; i < keys.length - 1; i++) {
    if (!cur[keys[i]]) cur[keys[i]] = {};
    cur = cur[keys[i]];
  }
  cur[keys[keys.length - 1]] = value;
}

function countKeys(obj: any): number {
  let count = 0;
  for (const key of Object.keys(obj)) {
    if (typeof obj[key] === 'object' && obj[key] !== null && !Array.isArray(obj[key])) {
      count += countKeys(obj[key]);
    } else {
      count++;
    }
  }
  return count;
}

describe('Apply services translations', () => {
  const files = [
    { name: 'zh.json', translations: ZH_SERVICES },
    { name: 'hi.json', translations: HI_SERVICES },
    { name: 'bn.json', translations: BN_SERVICES },
  ];

  for (const { name, translations } of files) {
    it(`should apply services translations to ${name}`, () => {
      const filePath = path.resolve(__dirname, `../i18n/locales/${name}`);
      const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
      
      // Replace services section entirely
      data.services = translations;
      
      fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf-8');
      
      const keyCount = countKeys(translations);
      console.log(`${name}: Applied ${keyCount} translated keys in services section`);
      expect(keyCount).toBeGreaterThan(100);
    });
  }
});
